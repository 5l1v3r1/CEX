void FFTM12T62::GenE(std::vector<byte> &E, IPrng* Random)
{
	size_t i;
	size_t j;
	size_t eq;
	ulong mask;
	std::vector<ushort> ind(T);

	while (1)
	{
		Random->Fill(ind, 0, ind.size());

		for (i = 0; i < T; i++) 
		{
			ind[i] &= ((ushort)1 << M) - 1;
		}

		eq = 0;
		for (i = 1; i < T; i++)
		{
			for (j = 0; j < i; j++)
			{
				if (ind[i] == ind[j])
				{
					eq = 1;
				}
			}
		}

		if (eq == 0)
			break;
	}

	std::array<ulong, T> val;
	for (j = 0; j < T; j++)
		val[j] = ((ulong)1 << (ind[j] & 63));

	std::array<ulong, 64> eInt;
	for (i = 0; i < 64; i++)
	{
		eInt[i] = 0;
		for (j = 0; j < T; j++)
		{
			mask = i ^ (ind[j] >> 6);
			mask -= 1;
			mask >>= 63;
			mask = ~mask + 1;
			eInt[i] |= val[j] & mask;
		}
	}

	IntUtils::LeToBlock(eInt, 0, E, 0, eInt.size() * sizeof(ulong));
}

